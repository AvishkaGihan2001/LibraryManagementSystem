    private void loadTable() {
        try {

            ArrayList<CategoryDto> categoryDtos = CATEGORY_CONTROLLER.getAll();
            for (CategoryDto categoryDto : categoryDtos) {
                txtCategoryID.addItem(categoryDto.getCategoryName());
            }

            String[] columns = {"Code", "Title", "Author", "Publisher", "Published Date", "Description", "Qauntity"};
            DefaultTableModel dtm = new DefaultTableModel(columns, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }

            };
            BookTable.setModel(dtm);

            ArrayList<BookDto> bookDtos = BOOK_CONTROLLER.getAll();
            for (BookDto bookDto : bookDtos) {
                Object[] rowData = {
                    bookDto.getCode(),
                    bookDto.getTitle(),
                    bookDto.getAuthor(),
                    bookDto.getPublisher(),
                    bookDto.getPublishedDate(),
                    bookDto.getDescription(),
                    bookDto.getQuantity()
                };
                dtm.addRow(rowData);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

