    private void register() {
        try {

            UserDto userDto = new UserDto(
                    txtcode.getText(),
                    txtfirstName.getText(),
                    txtlastName.getText(),
                    txtUserName.getText(),
                    txtPassword.getText()
            );

            String resp = USER_CONTROLLER.register(userDto);
            JOptionPane.showMessageDialog(this, resp);
            clear();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }

    private void clear() {
        txtcode.setText("");
        txtfirstName.setText("");
        txtlastName.setText("");
        txtUserName.setText("");
        txtPassword.setText("");
    }
