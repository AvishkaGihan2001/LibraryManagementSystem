    private void searchBook() {
        try {
            String Code = txtBookCode.getText();
            BookDto bookDto = BOOK_CONTROLLER.get(Code);

            if (bookDto != null) {
                lblBookDetails.setText(bookDto.getTitle() + " | " + bookDto.getDescription());
            } else {
                lblBookDetails.setText("Item Not Found");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void searchMember() {
        try {
            String Code = txtMemberCode.getText();
            MemberDto memberDto = MEMBER_CONTROLLER.get(Code);

            if (memberDto != null) {
                lblMemberDetails.setText(memberDto.getFirstName() + " | " + memberDto.getLastName() + " | " + memberDto.getAddress());
            } else {
                lblMemberDetails.setText("Item Not Found");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void placeTransaction() {
        try {
            TransactionDto transactionDto = new TransactionDto(
                    txtTransactionCode.getText(),
                    txtBookCode.getText(),
                    txtMemberCode.getText(),
                    txtBorrowDate.getText(),
                    txtDueDate.getText()
            );

            String resp = TRANSACTION_CONTROLLER.save(transactionDto);
            JOptionPane.showMessageDialog(this, resp);
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void clear() {
        txtTransactionCode.setText("");
        txtBookCode.setText("");
        txtMemberCode.setText("");
        txtBorrowDate.setText("");
        txtDueDate.setText("");
        lblBookDetails.setText("");
        lblMemberDetails.setText("");
    }

    private void loadTable() {
        try {
            String[] columns = {"Code", "Book", "Member", "Borrow Date", "Due Date", "Status"};
            DefaultTableModel dtm = new DefaultTableModel(columns, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }

            };
            TransactionTable.setModel(dtm);

            String status = "";

            ArrayList<TransactionDto> transactionDtos = TRANSACTION_CONTROLLER.getAll();
            for (TransactionDto transactionDto : transactionDtos) {
                
                if (transactionDto.getFine() > 0) {
                    status = "Not Paid";
                }else{
                    status = "Completed";
                }
                Object[] rowData = {
                    transactionDto.getTransactionCode(),
                    transactionDto.getBookCode(),
                    transactionDto.getMemberCode(),
                    transactionDto.getBorrowDate(),
                    transactionDto.getDueDate(),
                    status
                };
                dtm.addRow(rowData);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
