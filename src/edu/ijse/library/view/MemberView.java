    private void save() {
        try {
            MemberDto memberDto = new MemberDto(
                    txtCode.getText(),
                    txtFirstName.getText(),
                    txtLastName.getText(),
                    txtPhone.getText(),
                    txtAddress.getText()
            );
            String resp = MEMBER_CONTROLLER.save(memberDto);
            JOptionPane.showMessageDialog(this, resp);
            clear();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }

    private void clear() {
        txtCode.setText("");
        txtFirstName.setText("");
        txtLastName.setText("");
        txtPhone.setText("");
        txtAddress.setText("");
    }

    private void update() {
        try {
            MemberDto memberDto = new MemberDto(
                    txtCode.getText(),
                    txtFirstName.getText(),
                    txtLastName.getText(),
                    txtPhone.getText(),
                    txtAddress.getText()
            );
            String resp = MEMBER_CONTROLLER.update(memberDto);
            JOptionPane.showMessageDialog(this, resp);
            clear();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void delete() {
        try {
            String code = txtCode.getText();
            String resp = MEMBER_CONTROLLER.delete(code);
            JOptionPane.showMessageDialog(this, resp);
            clear();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void search() {
        try {
            String code = txtCode.getText();
            MemberDto memberDto = MEMBER_CONTROLLER.get(code);

            if (memberDto != null) {
                txtFirstName.setText(memberDto.getFirstName());
                txtLastName.setText(memberDto.getLastName());
                txtPhone.setText(memberDto.getPhone());
                txtAddress.setText(memberDto.getAddress());

            } else {
                JOptionPane.showMessageDialog(this, "Member Not Found");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

