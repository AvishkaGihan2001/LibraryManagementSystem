    private void clear() {
        txtUserName.setText("");
        txtPassword.setText("");
    }

    private void login() {
        try {
            UserDto userDto = new UserDto(
                    txtUserName.getText(),
                    txtPassword.getText()
            );

            String resp = USER_CONTROLLER.login(userDto);
            JOptionPane.showMessageDialog(this, resp);
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
