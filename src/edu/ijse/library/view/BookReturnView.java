    private void search() {
        try {

            String code = txtTransactionCode.getText();
            TransactionDto transactionDto = TRANSACTION_CONTROLLER.get(code);

            String bookCode = transactionDto.getBookCode();
            BookDto bookDto = BOOK_CONTROLLER.get(bookCode);

            if (bookDto != null) {
                lblBookDetails.setText(bookDto.getTitle() + " | " + bookDto.getDescription());
            } else {
                lblBookDetails.setText("Book Not Found");
            }

            String memberCode = transactionDto.getMemberCode();
            MemberDto memberDto = MEMBER_CONTROLLER.get(memberCode);

            if (memberDto != null) {
                lblMemberDetails.setText(memberDto.getFirstName() + " | " + memberDto.getLastName() + " | " + memberDto.getAddress());
            } else {
                lblMemberDetails.setText("Member Not Found");
            }

            if (transactionDto != null) {
                lblDueDate.setText(transactionDto.getDueDate());

            } else {
                JOptionPane.showMessageDialog(this, "Not Found");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void completeTransaction() {
        try {
            String returnDateString = LocalDate.now().toString();
            String dueDateString = lblDueDate.getText();  // Assuming lblDueDate is a JLabel or similar component

            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");

            LocalDate returnDate = LocalDate.parse(returnDateString, formatter);
            LocalDate dueDate = LocalDate.parse(dueDateString, formatter);

            long daysBetween = ChronoUnit.DAYS.between(dueDate, returnDate);

            if (daysBetween > 0) {

                double fine = daysBetween * 20;
                TransactionDto transactionDto = new TransactionDto(
                        txtTransactionCode.getText(),
                        returnDateString,
                        fine
                );

                String resp = TRANSACTION_CONTROLLER.completeTransaction(transactionDto);
                if (fine > 0) {
                    String[] options = {"Pay Now", "Pay Later"};
                    int choice = JOptionPane.showOptionDialog(
                            this,
                            "Fine amount: " + fine,
                            "Fine Payment",
                            JOptionPane.YES_NO_OPTION,
                            JOptionPane.INFORMATION_MESSAGE,
                            null,
                            options,
                            options[0]
                    );

                    FineDto fineDto = new FineDto(
                            txtTransactionCode.getText(),
                            fine,
                            false
                    );

                    if (choice == JOptionPane.YES_OPTION) {
                        fineDto.setPaid(true);
                    } else {
                        fineDto.setPaid(false);
                    }

                    String fineResp = TRANSACTION_CONTROLLER.save(fineDto);

                    JOptionPane.showMessageDialog(this, fineResp);

                    if (fineResp.equals("Success")) {

                        String transactionCode = txtTransactionCode.getText();
                        TransactionDto transactionDto1 = TRANSACTION_CONTROLLER.get(transactionCode);

                        String bookCode = transactionDto1.getBookCode();
                        BookDto bookDto = BOOK_CONTROLLER.get(bookCode);

                        int quantity = bookDto.getQuantity();

                        BookDto bookDto1 = new BookDto(
                                bookCode,
                                quantity + 1
                        );

                        BOOK_CONTROLLER.updateQuantity(bookDto1);
                    } else {
                        JOptionPane.showMessageDialog(this, "Failed to update fine");
                    }

                } else {
                    JOptionPane.showMessageDialog(this, resp);
                }
            }

            clear();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

